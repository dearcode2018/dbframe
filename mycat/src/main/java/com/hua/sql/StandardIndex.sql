/* 索引分析 */

/*
在不读取整个表的情况下，索引使db应用程序可以
更快地查找数据.

用户无法看到索引，它们只能被用来加速搜索/查询

更新一个包含索引的表需要比更新一个没有索引的表更多的时间，
这是由于索引本身也需要更新。
因此，理想的做法是仅仅在常常被搜索的列（以及表）上面创建索引。
*/

/* ====================  ==================== */

/* 创建索引 */

/* 创建 普通 索引 */
CREATE INDEX index_name ON tb_name (column_name ASC | DESC, ...);

/* 创建 唯一 索引 */
/* 索引的两行不能拥有相同的值 */
CREATE UNIQUE INDEX index_name ON tb_name (column_name ASC | DESC, ...);



/* ====================  ==================== */

/* 删除索引 */
DROP INDEX index_name;

/* ====================  ==================== */

/* 修改索引 */
ALTER INDEX index_name;



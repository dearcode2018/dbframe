/* 存储过程 */

/*
Stored Procedure 存储过程是一组为了完成特定功能的sql语句集合，经过编译后存储在
数据库中，用户通过指定的存储过程名称和参数来调用执行.

一个存储过程是一个可编程的函数，数据库中的存储过程可以看作面向对象方法的封装模拟.

存储过程对外就是一个方法，通过名称和参数来执行调用.

*/

/* ====================  ==================== */

/* 存储过程优点 */
/*
1) 增强了sql语言的功能和灵活性
存储过程利用流程控制语句编写，有很强的灵活性，可以完成复杂的判断和运算.

2) 存储过程对复杂数据业务进行了封装，很好屏蔽了底层的复杂性.

3) 存储过程可以实现较快的执行速度: 相比Transaction-Sql速度快，因为存储过程只需编译一次，
以后直接执行即可，而Transaction-Sql语句每次运行都要进行编译和优化，速度较慢.

4) 减少网络流量: 客户机调用存储过程，只需传送调用语句即可，可以大大降低应用服务器和
数据库服务器的网络负载.

5) 安全: 存储过程可以作为一种安全机制来充分使用，可以有效限制非法用户的访问.

*/


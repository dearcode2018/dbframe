/* SELECT INTO */
/*
从一个表中取出数据，然后将数据插入另一个张表，
这2张表的表结构一致 或者 返回的列表需要和新表保持一致.
*/
SELECT * | view_field_list INTO tb_name_b
FROM tb_name_a
WHERE filter_condition_list;

/* 表数据备份 */
SELECT * INTO tb_name_back
FROM tb_name
WHERE filter_condition_list;

/* 拷贝到其他数据库 */
SELECT * INTO tb_name_back IN 'other_db'
FROM tb_name
WHERE filter_condition_list;

/* 从多个表的结果集拷贝到一个张表 */
SELECT view_field_list INTO tb_name_back IN 'other_db'
FROM tb_name_list
WHERE filter_condition_list;


